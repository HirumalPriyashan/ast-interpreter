package CSEMachine.Symbols;

/**
 * Class representation for Rand symbols in control stack
 * 
 * @author Hirumal Priyashan
 * @version 1.0
 * @since 1.0
 */
public abstract class Rand extends Symbol{

    /**
     * Class constructor.
     * 
     * @param token token for the symbol
     */
    public Rand(String token) {
        super(token);
    }
    
}
